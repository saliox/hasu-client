package io.github.solclient.client.mod.impl.core.mixins;

import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.gen.Accessor;

import net.minecraft.world.level.LevelProperties;

/**
 * Access to the raw world time field (Azur Client) - bypasses the
 * getTimeOfDay() interception used by the Time Changer mod, so that the day
 * counter HUD always shows the real world day.
 */
@Mixin(LevelProperties.class)
public interface LevelPropertiesAccessor {

	@Accessor("timeOfDay")
	long azur$getRawTimeOfDay();

}
